package Unidad_5;
public class cssFilaLagrange {
    private double i, x, fx;

    public double getI() {
        return i;
    }

    public void setI(double i) {
        this.i = i;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getFx() {
        return fx;
    }

    public void setFx(double fx) {
        this.fx = fx;
    }

    @Override
    public String toString() {
        return "cssFilaLagrange{" + "i=" + i + ", x=" + x + ", fx=" + fx + '}';
    }
    

}
