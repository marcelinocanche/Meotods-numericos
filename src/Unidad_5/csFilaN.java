
package Unidad_5;

public class csFilaN {
    
    private int i;
    private double x;
    private double y;

    public int getI() {
        return i;
    }

    public void setI(int i) {
        this.i = i;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }

    @Override
    public String toString() {
        return "csFilaN{" + "i=" + i + ", x=" + x + ", y=" + y + '}';
    }

}
